# Lesson Name: Introduction to Arrays
# Goal: Learn how to store and access multiple values using arrays (lists).

numbers = [10, 20, 30, 40, 50]
print(numbers)

fruits = ["Apple", "Mango", "Banana"]
print(fruits[1])

marks = [85, 90, 78, 92]
for mark in marks:
    print(mark)

# Summary:
# Learned how to create, access and display array elements.
