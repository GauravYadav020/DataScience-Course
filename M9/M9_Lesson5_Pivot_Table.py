# ============================================================
# M9 Lesson 5 – Pivot Table
# ============================================================
# A Pivot Table summarizes large datasets quickly without writing
# formulas. It helps group, filter, and analyze data efficiently.
# ============================================================


# ----------------------------
# Activity 1: Create Your First Pivot Table
# ----------------------------
# Goal: Understand the structure and purpose of a Pivot Table.
# Summary: Students will insert a Pivot Table from a sample sales
#          dataset and explore the Rows, Columns, and Values fields.

# Task:
# - Create a dataset with columns: Date, Salesperson, Region, Sales Amount
#   (at least 10 rows of sample data)
# - Select the entire dataset
# - Go to Insert > Pivot Table > New Sheet
# - In the Pivot Table Editor, add "Salesperson" to Rows
# - Add "Sales Amount" to Values (set to SUM)
# - Observe the summarized output


# ----------------------------
# Activity 2: Filter and Group Pivot Data
# ----------------------------
# Goal: Use Filters and grouping to drill down into specific data.
# Summary: Students will add filters to their Pivot Table to view
#          data for specific regions or time periods only.

# Task:
# - Add "Region" to the Filters section in the Pivot Table Editor
# - Filter to show only one Region (e.g., North)
# - Now move "Region" to Columns to see a cross-tabulation
# - Change the Values summary from SUM to AVERAGE
# - Observe how the Pivot Table updates instantly


# ----------------------------
# Activity 3: Analyze with Pivot Table
# ----------------------------
# Goal: Use Pivot Tables to answer real business questions.
# Summary: Students will use their Pivot Table to extract meaningful
#          insights and answer specific data questions.

# Task:
# - Using your Pivot Table, answer these questions:
#   Q1: Which salesperson had the highest total sales?
#   Q2: Which region generated the most revenue?
#   Q3: What was the average sale per region?
# - Add a second Pivot Table on a new sheet showing: Month vs Region sales
# - Write your findings as comments directly in the sheet
