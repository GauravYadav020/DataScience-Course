# ============================================================
# M9 Lesson 4 – Charts in Google Sheets
# ============================================================
# Charts help visualize data to find trends, compare values,
# and communicate insights more effectively.
# ============================================================


# ----------------------------
# Activity 1: Create a Basic Chart
# ----------------------------
# Goal: Learn how to insert and customize a basic chart.
# Summary: Students will select a dataset and insert a chart,
#          then explore different chart types and their uses.

# Task:
# - Enter data: Column A = Months (Jan–Jun), Column B = Sales (any values)
# - Select both columns (A1:B7)
# - Go to Insert > Chart
# - Google Sheets will auto-suggest a chart type
# - Try switching between: Bar Chart, Line Chart, and Pie Chart
# - Note which chart type best represents this data and why


# ----------------------------
# Activity 2: Customize Chart Design
# ----------------------------
# Goal: Modify chart titles, colors, and labels for clarity.
# Summary: Students will use the Chart Editor to make their
#          chart more readable and visually appealing.

# Task:
# - Double-click on your chart to open the Chart Editor
# - Go to "Customize" tab
# - Change the Chart Title to "Monthly Sales Report"
# - Change the bar/line color to your preferred color
# - Add data labels to show values on each bar or point
# - Move the chart to a new sheet using: 3-dot menu > Move to own sheet


# ----------------------------
# Activity 3: Create a Comparison Chart
# ----------------------------
# Goal: Compare multiple data series in a single chart.
# Summary: Students will add a second data series to their chart
#          to visually compare two sets of data side by side.

# Task:
# - Add Column C = Target Sales (set fixed target values like 500 each month)
# - Select A1:C7 and insert a new chart
# - Choose "Combo Chart" to show Sales as bars and Target as a line
# - Label both series clearly in the chart legend
# - Analyze: In which months did actual sales exceed the target?
