# ============================================================
# M9 Lesson 1 – Introduction to Google Sheets
# ============================================================
# Google Sheets is a free, cloud-based spreadsheet tool by Google.
# It allows you to organize, analyze, and share data easily online.
# ============================================================


# ----------------------------
# Activity 1: Explore the Interface
# ----------------------------
# Goal: Get familiar with the Google Sheets interface and basic navigation.
# Summary: Students will open Google Sheets, identify key areas like
#          the toolbar, formula bar, rows, columns, and cells.

# Task:
# - Open Google Sheets from your Google Drive
# - Identify: Menu Bar, Toolbar, Formula Bar, Cell Reference Box
# - Click on different cells and observe the Cell Reference Box
# - Rename the sheet tab at the bottom to "My First Sheet"


# ----------------------------
# Activity 2: Enter and Format Data
# ----------------------------
# Goal: Learn how to enter data and apply basic formatting.
# Summary: Students will type data into cells and use formatting
#          options like bold, font size, and background color.

# Task:
# - In Column A, type: Name, Alice, Bob, Charlie, Diana
# - In Column B, type: Score, 85, 90, 78, 95
# - Make Row 1 (Name, Score) Bold and change its background color
# - Adjust column width so all text fits properly


# ----------------------------
# Activity 3: Save and Share a Sheet
# ----------------------------
# Goal: Understand how to save and collaborate using Google Sheets.
# Summary: Students will learn that Sheets auto-saves to Google Drive
#          and practice sharing a file with view/edit permissions.

# Task:
# - Check that your sheet is saved (look for "All changes saved" message)
# - Click the "Share" button (top-right corner)
# - Share your sheet with a classmate using their email (View Only)
# - Explore the difference between "Viewer", "Commenter", and "Editor" roles
